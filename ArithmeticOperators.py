# Arithmetic Operators in Python Practice (Python Operators)

# Global Variable X,Y
x = 8
y = 7

# Addition
totalSum = x + y
print(totalSum)

# Subtraction
totalMinus = x - y
print(totalMinus)

# Multiplication
totalMulti = x * y
print(totalMulti)

# Division
totalDivision = x / y
print(totalDivision)

# Modulus
totalModulus = x % y
print(totalModulus)

# Exponentiation
totalExponentiation = x ** y
print(totalExponentiation)

# Flor Division
totalFlorDivision = x // y
print(totalFlorDivision)
