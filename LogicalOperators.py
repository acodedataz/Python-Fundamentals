# Logical Operators in Python Practice (Python Operators)

# Logical Operators Name
# and Logical Operators #Returns True if both statements are true
# or Logical Operators #Returns True if one of the statements is true
# not Logical Operators #Reverse the result, returns False if the result is true

# Global Variable
x = 5

# print and statement and
print(x > 2 and x < 10)

# print and statement or
print(x < 3 or x < 8)

# print and statement not
print(not (x > 3 and x < 10))


# Python Swapping System (SWAPPING)

#Global Variable
c = 50
d = 100
c,d = d,c # swapping processing this line
print(f"This is Variable Value is C: {c} \n{f"This is Variable Value is D: {d}"}")
