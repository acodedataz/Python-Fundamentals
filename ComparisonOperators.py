# Comparison Operators in Python Practice (Python Operators)

# Comparison Operators Name
# (1) == Name : Equal
# (2) !- Name : Not Equal
# (3) > Name : Greater than
# (4) < Name : Less than
# (5) >= Name : Greater than or Equal
# (6) <= Name : Less than or Equal

# Example Here in Python Comparison Operators
# Global Variable
x = 10
y = 20

# Print & Condition Comparison Operators
print(x == y)  # 10 == 20 = False
print(x != y)  # 10 != 20 = True
print(x > y)  # 10 > 20 = False
print(x < y)  # 10 < 20 = True
print(x >= y)  # 10 >= 20 = False
print(x <= y)  # 10 <= 20 = True
