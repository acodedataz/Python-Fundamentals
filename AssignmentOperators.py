# Assignment Operators in Python Practice (Python Operators)

# Addition AssignmentOperators
x = 5
x += 8
print(x)

# Subtraction AssignmentOperators
p = 5
p -= 2
print(p)

# Multiplication AssignmentOperators
q = 9
q *= 3
print(q)

# Division AssignmentOperators
w = 8
w /= 5
print(w)

# Modulus AssignmentOperators
j = 5
j %= 3
print(j)

# Exponentiation AssignmentOperators
f = 8
f **= 4
print(f)

# Flor Division AssignmentOperators
d = 3
d //= 2
print(d)

# Bitwise AND AssignmentOperator
s = 3
s &= 2
print(s)

# Bitwise OR AssignmentOperator
a = 8
a |= 3
print(a)

# Bitwise XOR AssignmentOperator
u = 3
u ^= 5
print(u)

# Right Shift AssignmentOperator
w = 40
w >>= 55
print(w)

# Left Shift AssignmentOperator
i = 88
i <<= 8
print(i)

# Walrus Assignment Operators
h = 8
print(h := 7)
